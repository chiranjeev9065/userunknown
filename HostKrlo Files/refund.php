    <p><strong>Last Updated: 07 Nov, 24</strong></p>

    <p>At HostKrlo, we strive to deliver exceptional web hosting services to meet our customers' needs. This policy explains our terms for cancellations and refunds on hosting plans.</p>

    <h2>1. Cancellation Policy</h2>
    <h3>• Service Cancellation</h3>
    <p>You may cancel your hosting services at any time through your account dashboard or by contacting our support team.</p>
    
    <h3>• Notice Period</h3>
    <p>We recommend initiating cancellations with a <strong>3-day</strong> notice to ensure a smooth process.</p>

    <h2>2. Refund Policy</h2>
    <h3>Eligibility for Refunds</h3>
    <p>Refunds are available on hosting plans based on the criteria below:</p>
    <ul>
        <li><strong>Monthly Plans:</strong> Eligible for a refund within 7 days of purchase, provided a valid reason is given.</li>
        <li><strong>Yearly Plans:</strong> Eligible for a refund within 30 days of purchase, provided a valid reason is given.</li>
    </ul>

    <h3>Valid Reasons for Refunds</h3>
    <p>Refunds are processed only for legitimate issues, such as:</p>
    <ul>
        <li>Persistent technical problems impacting website uptime or performance, unresolved by our support team.</li>
        <li>Any unmet service standards as outlined in the initial purchase agreement.</li>
    </ul>

    <h3>Non-Refundable Services</h3>
    <p>Certain add-ons and services, including but not limited to domain registrations and SSL certificates, are non-refundable.</p>

    <h2>3. Refund Processing</h2>
    <h3>• Timeline</h3>
    <p>Refunds are processed within <strong>45 business days</strong> after a valid refund request is confirmed.</p>

    <h3>• Mode of Refund</h3>
    <p>Refunds will be credited back to the original payment method used during purchase.</p>

